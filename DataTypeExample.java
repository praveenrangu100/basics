package basics;

public class DataTypeExample {

	public static void main(String[] args) {
		//datatype variablename = value;
		int number = 23;
		System.out.println("Value is " + number);//+ wierd operator
		
		byte ByteNumber = 122;
		System.out.println("Byte number is " + ByteNumber);
		
		short ShortNumber = 5456;
		System.out.println("Short value is " + ShortNumber);
		
		long LongNumber = 87853;
		System.out.println("Long number is " + LongNumber);
		
		float FloatNumber = 13.1234f;
		System.out.println("Float value is " + FloatNumber);
		
		double DoubleNumber = 12.32156;
		System.out.println("Value is " + DoubleNumber);
		
		String stringname = "Praveen";
		System.out.println("Name : " + stringname);
		
		String branch = "Mechaniacal";
		System.out.println("Branch : " + branch);
		
		char gender = 'M';
		System.out.println("Gender : " + gender);
		
		long mobile = 9515218758L;
		System.out.println("Mobile no : " + mobile);
		
		float weight = 45.0f;
		System.out.println("Weight : " + weight);
		
		byte age = 22;
		System.out.println("Age : " + age);
		
		
	}

}
